async function sha256(value) {
  if (window.crypto?.subtle && window.TextEncoder) {
    const data = new TextEncoder().encode(value);
    const digest = await crypto.subtle.digest("SHA-256", data);
    return Array.from(new Uint8Array(digest)).map((byte) => byte.toString(16).padStart(2, "0")).join("");
  }
  return sha256Fallback(value);
}

function sha256Fallback(value) {
  const rightRotate = (word, amount) => (word >>> amount) | (word << (32 - amount));
  const bytes = [];
  const encoded = unescape(encodeURIComponent(value));
  for (let i = 0; i < encoded.length; i += 1) bytes.push(encoded.charCodeAt(i));

  const bitLength = bytes.length * 8;
  bytes.push(0x80);
  while ((bytes.length % 64) !== 56) bytes.push(0);
  for (let i = 7; i >= 0; i -= 1) bytes.push((bitLength / (2 ** (i * 8))) & 0xff);

  const constants = [
    0x428a2f98, 0x71374491, 0xb5c0fbcf, 0xe9b5dba5, 0x3956c25b, 0x59f111f1, 0x923f82a4, 0xab1c5ed5,
    0xd807aa98, 0x12835b01, 0x243185be, 0x550c7dc3, 0x72be5d74, 0x80deb1fe, 0x9bdc06a7, 0xc19bf174,
    0xe49b69c1, 0xefbe4786, 0x0fc19dc6, 0x240ca1cc, 0x2de92c6f, 0x4a7484aa, 0x5cb0a9dc, 0x76f988da,
    0x983e5152, 0xa831c66d, 0xb00327c8, 0xbf597fc7, 0xc6e00bf3, 0xd5a79147, 0x06ca6351, 0x14292967,
    0x27b70a85, 0x2e1b2138, 0x4d2c6dfc, 0x53380d13, 0x650a7354, 0x766a0abb, 0x81c2c92e, 0x92722c85,
    0xa2bfe8a1, 0xa81a664b, 0xc24b8b70, 0xc76c51a3, 0xd192e819, 0xd6990624, 0xf40e3585, 0x106aa070,
    0x19a4c116, 0x1e376c08, 0x2748774c, 0x34b0bcb5, 0x391c0cb3, 0x4ed8aa4a, 0x5b9cca4f, 0x682e6ff3,
    0x748f82ee, 0x78a5636f, 0x84c87814, 0x8cc70208, 0x90befffa, 0xa4506ceb, 0xbef9a3f7, 0xc67178f2
  ];
  let hash = [0x6a09e667, 0xbb67ae85, 0x3c6ef372, 0xa54ff53a, 0x510e527f, 0x9b05688c, 0x1f83d9ab, 0x5be0cd19];

  for (let chunk = 0; chunk < bytes.length; chunk += 64) {
    const words = new Array(64);
    for (let i = 0; i < 16; i += 1) {
      const offset = chunk + i * 4;
      words[i] = ((bytes[offset] << 24) | (bytes[offset + 1] << 16) | (bytes[offset + 2] << 8) | bytes[offset + 3]) >>> 0;
    }
    for (let i = 16; i < 64; i += 1) {
      const s0 = rightRotate(words[i - 15], 7) ^ rightRotate(words[i - 15], 18) ^ (words[i - 15] >>> 3);
      const s1 = rightRotate(words[i - 2], 17) ^ rightRotate(words[i - 2], 19) ^ (words[i - 2] >>> 10);
      words[i] = (words[i - 16] + s0 + words[i - 7] + s1) >>> 0;
    }

    let [a, b, c, d, e, f, g, h] = hash;
    for (let i = 0; i < 64; i += 1) {
      const s1 = rightRotate(e, 6) ^ rightRotate(e, 11) ^ rightRotate(e, 25);
      const ch = (e & f) ^ ((~e) & g);
      const temp1 = (h + s1 + ch + constants[i] + words[i]) >>> 0;
      const s0 = rightRotate(a, 2) ^ rightRotate(a, 13) ^ rightRotate(a, 22);
      const maj = (a & b) ^ (a & c) ^ (b & c);
      const temp2 = (s0 + maj) >>> 0;
      h = g;
      g = f;
      f = e;
      e = (d + temp1) >>> 0;
      d = c;
      c = b;
      b = a;
      a = (temp1 + temp2) >>> 0;
    }
    hash = hash.map((item, index) => (item + [a, b, c, d, e, f, g, h][index]) >>> 0);
  }

  return hash.map((item) => item.toString(16).padStart(8, "0")).join("");
}

function requestServerAction(action, data = {}, options = {}) {
  if (!RESULTS_ENDPOINT) return Promise.reject(new Error("endpoint is empty"));
  const timeout = options.timeout || 15000;
  const callbackName = `aiCourseAuth_${Date.now()}_${Math.random().toString(36).slice(2)}`;
  const url = new URL(RESULTS_ENDPOINT);
  url.searchParams.set("action", action);
  url.searchParams.set("callback", callbackName);
  Object.entries(data).forEach(([key, value]) => {
    if (value === undefined || value === null) return;
    url.searchParams.set(key, typeof value === "string" ? value : JSON.stringify(value));
  });

  return new Promise((resolve, reject) => {
    const script = document.createElement("script");
    const timer = setTimeout(() => {
      cleanup();
      reject(new Error("auth timeout"));
    }, timeout);

    function cleanup() {
      clearTimeout(timer);
      script.remove();
      delete window[callbackName];
    }

    window[callbackName] = (response) => {
      cleanup();
      if (response?.ok) {
        resolve(response);
        return;
      }
      reject(new Error(response?.error || "server error"));
    };

    script.onerror = () => {
      cleanup();
      reject(new Error("auth request failed"));
    };

    script.src = url.toString();
    document.body.appendChild(script);
  });
}

function requestAuth(action, data) {
  return requestServerAction(action, {
    name: data.name,
    department: data.department || "",
    passwordHash: data.passwordHash
  }, { timeout: 20000 });
}

