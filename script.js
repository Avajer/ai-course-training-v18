function applyCourseUpgrade() {
  modules.forEach((module) => {
    const upgrade = courseUpgrades[module.id];
    if (upgrade) Object.assign(module, upgrade);
    const upgradeV2 = courseUpgradesV2[module.id];
    if (upgradeV2) Object.assign(module, upgradeV2);
    if (advancedQuizBank[module.id]) module.quiz = advancedQuizBank[module.id];
    module.quiz = module.quiz.map((question, questionIndex) => rotateQuestionOptions(question, questionIndex + modules.indexOf(module)));
  });
}

function rotateQuestionOptions(question, seed) {
  const options = question.options.map((option, index) => ({ option, index }));
  const shift = (seed % options.length) || 1;
  const rotated = options.slice(shift).concat(options.slice(0, shift));
  return {
    ...question,
    options: rotated.map((item) => item.option),
    answer: rotated.findIndex((item) => item.index === question.answer)
  };
}

