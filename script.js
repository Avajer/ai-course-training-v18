function getLevel(percent) {
  if (percent < 50) return "Начальный уровень. Нужно повторить основы промптов и безопасность.";
  if (percent < 75) return "Базовый пользователь. Можно применять ИИ в простых рабочих задачах.";
  if (percent < 90) return "Уверенный пользователь. Можно переходить к сложным промптам и работе с документами.";
  return "Продвинутый старт. Можно помогать коллегам и собирать библиотеку промптов.";
}

function getRecommendations(percent) {
  const wrongCategories = finalQuestions.reduce((acc, question, index) => {
    if (Number(state.finalAnswers[index]) !== question.answer) {
      acc[question.category] = (acc[question.category] || 0) + 1;
    }
    return acc;
  }, {});

  const recommendations = [];
  if (wrongCategories.prompt) recommendations.push("Ошибки в промптах: повторите блоки про промпт, формулу запроса, сложность задач и итерации.");
  if (wrongCategories.security) recommendations.push("Ошибки в безопасности: повторите блок безопасности и правило обезличивания данных.");
  if (wrongCategories.tools) recommendations.push("Ошибки в выборе нейросетей: повторите блок анализа инструментов и отдельный блок Qwen AI.");
  if (wrongCategories.verification) recommendations.push("Ошибки в проверке результата: повторите блок проверки ответа и используйте промпт самопроверки.");
  if (wrongCategories.docs) recommendations.push("Ошибки в работе с документами: повторите блок документов и шаблоны проверки текста.");
  if (percent < 50) recommendations.push("Общий результат низкий: пройдите курс заново и выполните практические задания после каждого блока.");
  if (percent >= 90) recommendations.push("Высокий результат: создайте личную библиотеку промптов для своих регулярных задач.");

  return recommendations.length ? recommendations : ["Результат ровный. Закрепите навык на реальной рабочей задаче и сохраните удачный промпт."];
}

function buildResultsPayload() {
  const correct = finalQuestions.reduce((sum, question, index) => {
    return sum + (Number(state.finalAnswers[index]) === question.answer ? 1 : 0);
  }, 0);
  const percent = Math.round((correct / finalQuestions.length) * 100);
  const categoryScores = finalQuestions.reduce((acc, question, index) => {
    if (!acc[question.category]) acc[question.category] = { correct: 0, total: 0 };
    acc[question.category].total += 1;
    if (Number(state.finalAnswers[index]) === question.answer) acc[question.category].correct += 1;
    return acc;
  }, {});
  const completedModules = modules
    .filter((module) => state.modules[module.id]?.submitted)
    .map((module) => ({ id: module.id, title: module.title }));
  const openAnswerRows = modules.flatMap((module) => (module.openQuestions || [])
    .map((question, index) => {
      const key = `${module.id}:${index}`;
      return {
        moduleId: module.id,
        moduleTitle: module.title,
        question,
        answer: state.openAnswers?.[key] || ""
      };
    })
    .filter((row) => row.answer.trim()));
  const practiceAnswerRows = modules
    .map((module) => ({
      moduleId: module.id,
      moduleTitle: module.title,
      task: module.practice,
      answer: state.practice?.[module.id] || ""
    }))
    .filter((row) => row.answer.trim());

  return {
    action: "submitResult",
    submittedAt: new Date().toISOString(),
    participant: state.participant,
    score: { correct, total: finalQuestions.length, percent, level: getLevel(percent) },
    categoryScores,
    completedModules,
    finalAnswers: finalQuestions.map((question, index) => ({
      question: question.q,
      selected: question.options[state.finalAnswers[index]],
      correct: question.options[question.answer],
      isCorrect: Number(state.finalAnswers[index]) === question.answer,
      category: question.category
    })),
    openAnswers: state.openAnswers,
    openAnswerRows,
    practiceAnswers: state.practice,
    practiceAnswerRows
  };
}

function validateParticipant() {
  const name = state.participant?.name?.trim();
  const department = state.participant?.department?.trim();
  if (!state.participant?.authenticated || !state.participant?.passwordHash) {
    showToast("Войдите или зарегистрируйтесь перед отправкой результата.");
    renderParticipantForm();
    participantView.scrollIntoView({ behavior: "smooth", block: "center" });
    return false;
  }
  if (!name || !department) {
    showToast("В учетной записи не хватает ФИО или подразделения.");
    renderParticipantForm();
    participantView.scrollIntoView({ behavior: "smooth", block: "center" });
    return false;
  }
  return true;
}

async function submitResults() {
  if (!state.finalSubmitted) {
    showToast("Сначала пройдите итоговый тест.");
    return;
  }
  if (!(await requireActiveParticipant())) return;
  if (!validateParticipant()) return;
  const payload = buildResultsPayload();

  if (!RESULTS_ENDPOINT) {
    state.resultStatus = "ошибка: не указана ссылка Google Apps Script";
    saveState();
    renderFinalTest();
    showToast("Ссылка отправки не указана. Скачайте файл результата или настройте Google Apps Script.");
    return;
  }

  state.resultStatus = "отправляется";
  saveState();
  renderFinalTest();

  try {
    const response = await fetch(RESULTS_ENDPOINT, {
      method: "POST",
      mode: "no-cors",
      headers: { "Content-Type": "text/plain;charset=utf-8" },
      body: JSON.stringify(payload)
    });
    state.resultStatus = response ? "отправлено на проверку" : "отправлено на проверку";
    saveState();
    renderFinalTest();
    showToast("Результат отправлен на проверку.");
  } catch (error) {
    state.resultStatus = "ошибка отправки";
    saveState();
    renderFinalTest();
    showToast("Не удалось отправить результат. Скачайте файл результата.");
  }
}

function downloadResultsJson() {
  if (!state.finalSubmitted) {
    showToast("Сначала пройдите итоговый тест.");
    return;
  }
  const blob = new Blob([JSON.stringify(buildResultsPayload(), null, 2)], { type: "application/json" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = `ai-course-result-${Date.now()}.json`;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

