function renderQuestion(moduleId, question, questionIndex, moduleState) {
  const selected = moduleState.answers?.[questionIndex];
  return `
    <div class="question mcq" data-question="${questionIndex}">
      <p>${questionIndex + 1}. ${question.q}</p>
      <div class="choices">
        ${question.options.map((option, optionIndex) => {
          const isSelected = Number(selected) === optionIndex;
          const resultClass = moduleState.submitted
            ? optionIndex === question.answer ? "is-correct" : isSelected ? "is-wrong" : ""
            : isSelected ? "is-selected" : "";
          return `<button class="choice-button ${resultClass}" type="button" data-module-id="${moduleId}" data-question-index="${questionIndex}" data-option-index="${optionIndex}">${option}</button>`;
        }).join("")}
      </div>
      ${moduleState.submitted ? `<div class="explanation">${question.why}</div>` : ""}
    </div>
  `;
}

function bindQuestionEvents(moduleId) {
  contentView.querySelectorAll(`[data-module-id="${moduleId}"]`).forEach((button) => {
    button.addEventListener("click", () => {
      const moduleState = state.modules[moduleId] || { answers: {}, submitted: false };
      if (moduleState.submitted) return;
      const questionIndex = button.dataset.questionIndex;
      moduleState.answers[questionIndex] = Number(button.dataset.optionIndex);
      state.modules[moduleId] = moduleState;
      saveState();
      contentView
        .querySelectorAll(`[data-module-id="${moduleId}"][data-question-index="${questionIndex}"]`)
        .forEach((choice) => choice.classList.toggle("is-selected", choice === button));
      button.classList.remove("is-pulsing");
      void button.offsetWidth;
      button.classList.add("is-pulsing");
    });
  });
}

async function submitModule(module) {
  if (!(await requireActiveParticipant())) return;
  const moduleState = state.modules[module.id] || { answers: {}, submitted: false };
  const answered = Object.keys(moduleState.answers).length;
  if (answered < module.quiz.length) {
    showToast("Ответьте на все вопросы мини-теста.");
    return;
  }

  moduleState.submitted = true;
  moduleState.score = module.quiz.reduce((sum, question, index) => {
    return sum + (Number(moduleState.answers[index]) === question.answer ? 1 : 0);
  }, 0);
  state.modules[module.id] = moduleState;
  saveState();
  renderModule(currentModuleIndex);
  updateProgress();
  void syncModuleResult(module);
}

function renderModuleResult(module, moduleState) {
  const percent = Math.round((moduleState.score / module.quiz.length) * 100);
  const text = percent === 100
    ? "Отлично. Можно двигаться дальше и применять прием на практике."
    : "Повторите пояснения к ошибочным вопросам и попробуйте применить блок на своей задаче.";
  const syncStatus = state.moduleSync?.[module.id] || "не отправлено";
  return `
    <div class="result-panel">
      <div class="result-score">${percent}%</div>
      <strong>${moduleState.score} из ${module.quiz.length} правильных ответов</strong>
      <p>${text}</p>
      <p><strong>Синхронизация в таблицу:</strong> ${syncStatus}</p>
    </div>
  `;
}

async function syncModuleResult(module) {
  if (!RESULTS_ENDPOINT || !validateParticipant({ quiet: true })) return;
  const moduleState = state.modules[module.id];
  if (!moduleState?.submitted) return;

  const percent = Math.round((moduleState.score / module.quiz.length) * 100);
  state.moduleSync[module.id] = "отправляется";
  saveState();
  if (currentView === "module" && modules[currentModuleIndex]?.id === module.id) {
    const resultNode = document.getElementById("moduleResult");
    if (resultNode) resultNode.innerHTML = renderModuleResult(module, moduleState);
  }

  try {
    await requestServerAction("submitModuleResult", {
      name: state.participant.name,
      department: state.participant.department || "",
      passwordHash: state.participant.passwordHash,
      submittedAt: new Date().toISOString(),
      build: COURSE_BUILD,
      moduleId: module.id,
      moduleTitle: module.title,
      correct: moduleState.score,
      total: module.quiz.length,
      percent
    }, { timeout: 20000 });

    state.moduleSync[module.id] = "отправлено";
    saveState();
    if (currentView === "module" && modules[currentModuleIndex]?.id === module.id) {
      const resultNode = document.getElementById("moduleResult");
      if (resultNode) resultNode.innerHTML = renderModuleResult(module, moduleState);
    }
  } catch (error) {
    state.moduleSync[module.id] = explainEndpointError(error);
    saveState();
    if (currentView === "module" && modules[currentModuleIndex]?.id === module.id) {
      const resultNode = document.getElementById("moduleResult");
      if (resultNode) resultNode.innerHTML = renderModuleResult(module, moduleState);
    }
    showToast("Мини-тест сохранён локально, но не отправлен в таблицу.");
  }
}

async function renderLibrary() {
  if (!(await requireActiveParticipant())) return;
  currentView = "library";
  contentView.innerHTML = `
    <section class="section-band">
      <p class="eyebrow">Рабочий набор</p>
      <h2>Библиотека готовых промптов</h2>
      <p>Замените поля в квадратных скобках на свои данные. Перед отправкой удалите персональную и конфиденциальную информацию.</p>
    </section>
    <section class="prompt-grid">
      ${promptLibrary.map((prompt, index) => `
        <article class="prompt-card">
          <div>
            <p class="eyebrow">${prompt.use}</p>
            <h3>${prompt.title}</h3>
          </div>
          <pre id="prompt-${index}">${escapeHtml(prompt.text)}</pre>
          <button class="copy-button" type="button" data-copy="${index}">Скопировать</button>
        </article>
      `).join("")}
    </section>
  `;

  contentView.querySelectorAll("[data-copy]").forEach((button) => {
    button.addEventListener("click", async () => {
      const prompt = promptLibrary[Number(button.dataset.copy)].text;
      try {
        if (navigator.clipboard?.writeText) {
          await navigator.clipboard.writeText(prompt);
        } else {
          copyWithFallback(prompt);
        }
        showToast("Промпт скопирован.");
      } catch {
        try {
          copyWithFallback(prompt);
          showToast("Промпт скопирован.");
        } catch {
          showToast("Не удалось скопировать автоматически. Выделите текст вручную.");
        }
      }
    });
  });

  renderNav();
  renderObjectives();
  renderRoadmap();
  prepareAnimations(contentView);
  window.scrollTo({ top: 0, behavior: "smooth" });
}

