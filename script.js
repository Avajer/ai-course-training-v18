function bindV2Delegation() {
  if (!contentView || contentView.dataset.v2Bound) return;
  contentView.dataset.v2Bound = "true";

  contentView.addEventListener("click", (event) => {
    const ddHead = event.target.closest(".dd-head");
    if (ddHead) {
      const item = ddHead.closest(".dd-item");
      const isOpen = item.classList.toggle("is-open");
      ddHead.setAttribute("aria-expanded", String(isOpen));
      return;
    }

    const checkItem = event.target.closest(".check-item");
    if (checkItem) {
      state.checks = state.checks || {};
      const key = checkItem.dataset.check;
      const next = !state.checks[key];
      state.checks[key] = next;
      checkItem.classList.toggle("is-checked", next);
      saveState();
      updateCheckProgress(key.split(":")[0]);
      return;
    }

    const anatomyRow = event.target.closest(".anatomy-row");
    if (anatomyRow) {
      const isOpen = anatomyRow.classList.toggle("is-open");
      anatomyRow.setAttribute("aria-expanded", String(isOpen));
    }
  });
}

